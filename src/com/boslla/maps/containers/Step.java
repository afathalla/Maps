package com.boslla.maps.containers;

public class Step {
	
	private int step;
	private String direction;
	
	public void setStep(int step)
	{
		this.step=step;
	}
	
	public void setDirection(String direction)
	{
		this.direction=direction;
	}
	
	public int getStep()
	{
		return step;
	}
	
	public String getDirection()
	{
		return direction;
	}
}